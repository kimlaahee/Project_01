package GAME_2;

public class VOClassW{

	private int num;
	private String word;
	
	public VOClassW() {}
	
	public VOClassW(int num, String word) { 
		super();
		this.num=num;
		this.word=word;

	}

	
	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	
	public String getWord() {
		return word;
	}

	public void setWord(String word) {
		this.word = word;
	}


}
